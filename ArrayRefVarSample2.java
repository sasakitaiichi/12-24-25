public class ArrayRefVarSample2 {
    public static void main(String[] args) {
        double myList[] = new double[10];
        System.out.println(myList[0]);

        char[] myCharList = new char[10];
        System.out.println(myCharList[0]);

        boolean[] myBooleanList = new boolean[10];
        System.out.println(myBooleanList[9]);
    }
}
